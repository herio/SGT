package br.com.sabrina.sgt.gerador.dto;

public class Dto14InformeMatricula {
	private String semestre;
	private String ano;
	private String professor;
	private String professor2;
	private String data;
	private String coordenador;
	
	public String getSemestre() {
		return semestre;
	}
	public void setSemestre(String semestre) {
		this.semestre = semestre;
	}
	public String getAno() {
		return ano;
	}
	public void setAno(String ano) {
		this.ano = ano;
	}
	public String getProfessor() {
		return professor;
	}
	public void setProfessor(String professor) {
		this.professor = professor;
	}
	public String getProfessor2() {
		return professor2;
	}
	public void setProfessor2(String professor2) {
		this.professor2 = professor2;
	}
	public String getData() {
		return data;
	}
	public void setData(String dia) {
		this.data = dia;
	}
	public String getCoordenador() {
		return coordenador;
	}
	public void setCoordenador(String coordenador) {
		this.coordenador = coordenador;
	}
		
}
